// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// count if certain letter appears x amount of times in a string

function countChars(string, letter) {
  var letterCount = 0
  // loop through word
  for(var i = 0; i < string.length; i++){
    if (string[i] === letter)
    letterCount +=1 
  }
return letterCount
}

